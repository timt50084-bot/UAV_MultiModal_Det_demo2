# 单机多卡启动脚本
#!/bin/bash
# ============================================================================
# 自动化消融实验流水线 (Ablation Study Pipeline)
# 作用: 按顺序自动跑完 Baseline 和 创新版，并将日志重定向保存，方便提取表格数据。
# 用法: bash scripts/run_ablation.sh
# ============================================================================

LOG_DIR="outputs/logs"
mkdir -p $LOG_DIR

echo "🚀 启动 24 小时无人值守消融实验流水线..."
echo "日志将自动保存到 ${LOG_DIR} 目录下"

# ---------------------------------------------------------
# 实验 1: 极简拼接 Baseline (无注意力，无对比损失)
# ---------------------------------------------------------
echo "================================================================="
echo "▶️ [实验 1/2] 正在运行 Baseline 对照组 (yolov8_dual_base.yaml)..."
echo "================================================================="
# 注意：这里我们强制它在单卡(device 0)上跑，你也可以改成调用上面的 dist_train.sh
python tools/train.py --config configs/model/yolov8_dual_base.yaml --device 0 > ${LOG_DIR}/ablation_base.log 2>&1
echo "✅ 实验 1 完成！日志已封存至 ${LOG_DIR}/ablation_base.log"


# ---------------------------------------------------------
# 实验 2: 满血绝杀版 Ours (双向 Transformer + 模态对齐)
# ---------------------------------------------------------
echo "================================================================="
echo "▶️ [实验 2/2] 正在运行 核心创新版 (yolov8_dual_crossattn.yaml)..."
echo "================================================================="
python tools/train.py --config configs/model/yolov8_dual_crossattn.yaml --device 0 > ${LOG_DIR}/ablation_crossattn.log 2>&1
echo "✅ 实验 2 完成！日志已封存至 ${LOG_DIR}/ablation_crossattn.log"

echo "================================================================="
echo "🎉 全线飘红！所有消融实验已执行完毕，快去写论文表格吧！"